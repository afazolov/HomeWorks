"""
Алгоритм сортировки выбором (Selection Sort)
---------------------------------------------
Принцип: делим список на два участка — отсортированный (слева)
и неотсортированный (справа). На каждом шаге находим минимальный
элемент в неотсортированной части и ставим его в конец
отсортированной части.

Временна́я сложность:
  Лучший случай  — O(n²)  (всегда ищет минимум по всей оставшейся части)
  Средний случай — O(n²)
  Худший случай  — O(n²)

Пространственная сложность: O(1)
"""


def selection_sort(lst: list) -> list:
    """
    Сортировка выбором.

    Принимает список, возвращает новый отсортированный список.
    Исходный список не изменяется.
    """
    arr = lst.copy()       # Работаем с копией, чтобы не менять оригинал
    n = len(arr)

    for i in range(n):
        # Считаем, что минимум — первый элемент неотсортированной части
        min_index = i

        # Ищем настоящий минимум в оставшейся (неотсортированной) части
        for j in range(i + 1, n):
            if arr[j] < arr[min_index]:
                min_index = j  # Нашли новый минимум — запоминаем его индекс

        # Ставим найденный минимум на своё место (обмен с позицией i)
        arr[i], arr[min_index] = arr[min_index], arr[i]

    return arr


# ---------------------------------------------------------------------------
# Демонстрация
# ---------------------------------------------------------------------------
if __name__ == "__main__":
    sample = [64, 34, 25, 12, 22, 11, 90]
    print(f"Исходный список:      {sample}")
    print(f"Отсортированный список: {selection_sort(sample)}")
