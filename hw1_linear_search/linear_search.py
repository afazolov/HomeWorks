"""
Алгоритм линейного поиска
--------------------------
Реализует линейный поиск и демонстрирует его работу.
"""


def linear_search(lst: list, target) -> int:
    """
    Линейный поиск элемента target в списке lst.

    Последовательно перебирает каждый элемент списка.
    Возвращает индекс первого вхождения target,
    или -1, если элемент не найден.

    Временна́я сложность:    O(n)
    Пространственная сложность: O(1)
    """
    for index, element in enumerate(lst):
        # Сравниваем текущий элемент с искомым
        if element == target:
            return index  # Нашли — возвращаем позицию
    return -1  # Прошли весь список, элемент не найден


# ---------------------------------------------------------------------------
# Быстрая демонстрация работы функции
# ---------------------------------------------------------------------------
if __name__ == "__main__":
    sample = [4, 2, 7, 1, 9, 3, 8, 5, 6, 0]

    for target in [7, 0, 42]:
        result = linear_search(sample, target)
        if result != -1:
            print(f"Элемент {target} найден по индексу {result}")
        else:
            print(f"Элемент {target} не найден (возвращено -1)")
