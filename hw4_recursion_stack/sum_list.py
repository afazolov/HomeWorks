if __name__ == "__main__":
    примеры = [
        [],
        [5],
        [1, 2, 3, 4, 5],
        [10, -3, 7, 2],
    ]

    for lst in примеры:
        print(f"sum_list({lst}) = {sum_list(lst)}")
