"""
K2 — Рекурсивное вычисление суммы элементов списка
----------------------------------------------------
Принцип рекурсии:
  sum_list([head, ...tail]) = head + sum_list(tail)
  sum_list([])              = 0   ← базовый случай
"""


def sum_list(lst: list) -> int | float:
    """
    Рекурсивно вычисляет сумму всех элементов списка.

    Параметры:
        lst — список чисел

    Возвращает:
        сумму всех элементов, или 0 если список пустой
    """
    # Базовый случай: пустой список — сумма равна 0
    # Без него рекурсия не остановится
    if len(lst) == 0:
        return 0

    # Рекурсивный случай:
    # берём первый элемент и прибавляем сумму оставшегося списка
    return lst[0] + sum_list(lst[1:])


