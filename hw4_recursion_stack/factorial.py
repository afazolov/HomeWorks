"""
K1 — Рекурсивное вычисление факториала
----------------------------------------
Факториал числа n (обозначается n!) — произведение всех
натуральных чисел от 1 до n включительно.

Пример: 5! = 5 × 4 × 3 × 2 × 1 = 120

Принцип рекурсии:
  factorial(n) = n × factorial(n - 1)
  factorial(0) = 1  ← базовый случай (останавливает рекурсию)
"""


def factorial(n: int) -> int:
    """
    Рекурсивно вычисляет факториал числа n.

    Параметры:
        n — неотрицательное целое число

    Возвращает:
        n! (факториал n)
    """
    # Базовый случай: факториал 0 и 1 равен 1
    # Без него рекурсия уходила бы в бесконечность
    if n <= 1:
        return 1

    # Рекурсивный случай: n! = n × (n-1)!
    return n * factorial(n - 1)


